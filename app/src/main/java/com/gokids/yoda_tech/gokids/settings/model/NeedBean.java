public class NeedBean {
    private String KidSpecialNeedID;
    private String KidSpecialNeed;
    private String SpecialNeed;
    private String SpecialNeedCategory;

    public String getKidSpecialNeedID() {
        return KidSpecialNeedID;
    }

    public void setKidSpecialNeedID(String kidSpecialNeedID) {
        KidSpecialNeedID = kidSpecialNeedID;
    }

    public String getKidSpecialNeed() {
        return KidSpecialNeed;
    }

    public void setKidSpecialNeed(String kidSpecialNeed) {
        KidSpecialNeed = kidSpecialNeed;
    }

    public String getSpecialNeed() {
        return SpecialNeed;
    }

    public void setSpecialNeed(String specialNeed) {
        SpecialNeed = specialNeed;
    }

    public String getSpecialNeedCategory() {
        return SpecialNeedCategory;
    }

    public void setSpecialNeedCategory(String specialNeedCategory) {
        SpecialNeedCategory = specialNeedCategory;
    }
}
